package doctolib_service.data.jpa.utils.model;

public class Daylight {
	 public int week;
	    public int wkday;
	    public int mon;
	    public int hour;
	    public int min;
	    public int sec;
		public int getWeek() {
			return week;
		}
		public void setWeek(int week) {
			this.week = week;
		}
		public int getWkday() {
			return wkday;
		}
		public void setWkday(int wkday) {
			this.wkday = wkday;
		}
		public int getMon() {
			return mon;
		}
		public void setMon(int mon) {
			this.mon = mon;
		}
		public int getHour() {
			return hour;
		}
		public void setHour(int hour) {
			this.hour = hour;
		}
		public int getMin() {
			return min;
		}
		public void setMin(int min) {
			this.min = min;
		}
		public int getSec() {
			return sec;
		}
		public void setSec(int sec) {
			this.sec = sec;
		}
	    
	    
}
